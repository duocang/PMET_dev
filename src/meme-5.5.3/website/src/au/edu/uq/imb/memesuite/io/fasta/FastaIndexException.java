package au.edu.uq.imb.memesuite.io.fasta;

public class FastaIndexException extends Exception {
  public FastaIndexException() {
    super();
  }

  public FastaIndexException(String message) {
    super(message);
  }

  public FastaIndexException(String message, Throwable cause) {
    super(message, cause);
  }

  public FastaIndexException(Throwable cause) {
    super(cause);
  }
}

